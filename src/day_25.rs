use core::cmp::Ordering;
use std::collections::HashSet;

fn part_1(source: &str) -> usize {
    let (keys, locks) = parse_input(source);

    let mut count = 0;

    for lock in locks.clone() {
        for key in keys.clone() {
            if lock
                .iter()
                .zip(key.clone())
                .map(|(a, b)| a + b)
                .max_by(|a, b| a.partial_cmp(b).unwrap_or(Ordering::Equal))
                .unwrap_or(0)
                <= 5
            {
                count += 1;
            }
        }
    }

    count
}

fn parse_input(source: &str) -> (HashSet<Vec<usize>>, HashSet<Vec<usize>>) {
    let raw_rows = source
        .split(['\n'])
        .filter(|v| v.len() > 0)
        .collect::<Vec<&str>>();

    let total_items = raw_rows.len() / 7;
    let mut keys: HashSet<Vec<usize>> = HashSet::new();
    let mut locks: HashSet<Vec<usize>> = HashSet::new();

    for i in 0..total_items {
        let offset = if raw_rows[i * 7].chars().filter(|&c| c == '#').count() == 5 {
            //lock
            1
        } else {
            0
        };

        let mut item: Vec<Vec<char>> = vec![];
        for j in 0..6 {
            item.push(raw_rows[i * 7 + j + offset].chars().collect());
        }
        let mut transpose: Vec<Vec<char>> = vec![vec!['.'; item.len()]; item[0].len()];
        for y in 0..item.len() {
            for x in 0..item[y].len() {
                transpose[x][y] = item[y][x];
            }
        }

        let final_item: Vec<usize> = transpose
            .iter()
            .map(|v| v.iter().filter(|&c| *c == '#').count())
            .collect();
        if offset == 1 {
            locks.insert(final_item.clone());
        } else {
            keys.insert(final_item.clone());
        }
    }

    (locks, keys)
}

